public class VariableDemo3 {
	/**
	 * 데이터 타입이란 ?
	 * - 메모리를 생성하기 위한 도구
	 * => 메모리 크기(size) 와 메모리 형태(type) 을 저장하기 위한 도구
	 * 
	 * 메모리를 얻고 사용하는 방법
	 * - 메모리 크기를 설정
	 * - 메모리에 식별자(고유 이름) 할당
	 * - 메모리에 데이터를 입력
	 * 
	 * 데이터 타입의 목적
	 * 1. 생성할 메모리 크기(size) 지정
	 * 2. 메모리의 형태(type) 을 지정
	 * 
	 * 사용례
	 * 1. 변수의 선언(4바이트 메모리 생성)
	 * int a;
	 * 2. 4바이트 메모리 타입에 맞는 값을 세팅(할당)
	 * a = 5;
	 * 
	 * 데이터 타입의 종류
	 * 기본형 (primitive type) 기본자료형(첫글자가 소문자)
	 * 참조형 (reference type) 객체자료형(첫글자가 대문자 (예)String)
	 * 
	 * [ 기본 자료형 ]
	 * 정수형 : byte < short < int < long
	 * 실수형 : float < double
	 * 논리형 : boolean
	 * 문자형 : char
	 */
}





